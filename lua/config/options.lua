local opt = vim.opt

-- =============================================================================
-- 编辑器外观
-- =============================================================================

-- 显示行号和相对行号
opt.number = true
opt.relativenumber = true

-- 当前行高亮
opt.cursorline = true

-- 第80列高亮线
opt.colorcolumn = "80"

-- 不显示模式提示（状态栏已显示）
opt.showmode = false

-- 显示不可见字符
opt.list = true
opt.listchars = {
  nbsp = "⦸",
  extends = "»",
  precedes = "«",
  tab = "▷⋯",
  trail = "•",
}

-- =============================================================================
-- 搜索配置
-- =============================================================================

-- 智能大小写匹配：小写搜索忽略大小写，包含大写时精确匹配
opt.ignorecase = true
opt.smartcase = true

-- 不高亮搜索结果（使用插件管理）
opt.hlsearch = false

-- =============================================================================
-- 缩进和格式化
-- =============================================================================

-- 使用空格代替Tab
opt.expandtab = true

-- Tab显示宽度和缩进宽度
opt.tabstop = 2
opt.shiftwidth = 2
opt.softtabstop = 2

-- 缩进对齐到shiftwidth的整数倍
opt.shiftround = true

-- 智能缩进
opt.smartindent = true

-- 自动缩进
opt.autoindent = true

-- =============================================================================
-- 折叠配置
-- =============================================================================

-- 基于缩进折叠
opt.foldmethod = "indent"

-- 启动时打开所有折叠
opt.foldlevelstart = 99

-- =============================================================================
-- 补全配置
-- =============================================================================

-- 补全菜单配置
opt.completeopt = "menu,menuone,noselect"

-- 补全菜单最大高度
opt.pumheight = 12

-- =============================================================================
-- 窗口和分屏
-- =============================================================================

-- 新窗口默认位置
opt.splitbelow = true
opt.splitright = true

-- 允许在未保存时切换缓冲区
opt.hidden = true

-- =============================================================================
-- 滚动配置
-- =============================================================================

-- 光标距离窗口边缘的行数
opt.scrolloff = 8
opt.sidescrolloff = 8

-- =============================================================================
-- 鼠标配置
-- =============================================================================

-- 启用鼠标
opt.mouse = "a"

-- 启用鼠标移动事件（可能影响性能）
opt.mousemoveevent = false

-- =============================================================================
-- 剪切板配置
-- =============================================================================

-- 与系统剪切板同步
opt.clipboard = "unnamedplus"

-- =============================================================================
-- 撤销和历史
-- =============================================================================

-- 持久化撤销历史
opt.undofile = true

-- =============================================================================
-- 其他配置
-- =============================================================================

-- 不换行显示
opt.wrap = false
